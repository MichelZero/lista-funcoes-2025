# # Problema 4: Geometria de um Círculo
# Em linguagem Python, faça um programa que, a partir do valor do raio de um círculo, 
# calcule e apresente sua área e sua circunferência (perímetro). 
# Considere o valor de PI = 3.14159.
# Fórmulas:
AREA = PI * (RAIO ** 2)
CIRCUNFERENCIA = 2 * PI * RAIO

# O programa deve apresentar o valor do raio, da área calculada e da circunferência calculada.
# OBS: o programa deve conter quatro funções:
# a) Função para ler o raio: não recebe parâmetro e retorna o valor do raio lido.
# b) Função para calcular a área: recebe como parâmetro o raio e retorna a área calculada.
# c) Função para calcular a circunferência: recebe como parâmetro o raio e retorna a circunferência calculada.
# d) Função para apresentar os resultados: recebe como parâmetros o raio, a área e a circunferência, e somente imprime os resultados.